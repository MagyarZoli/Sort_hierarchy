export enum SortType {

    /**
     * Puts items in ascending order.
     */
    INCREASING,

    /**
     * Places the elements in descending order.
     */
    DECREASING,

    /**
     * Does not change the order of the elements!
     */
    DO_NOT_CHANGE_IT,

    /**
     * It reverses the order of the elements, what was first becomes last.
     */
    REVERSE_ORDER,
}